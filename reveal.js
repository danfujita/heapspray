<p>You can add your own extensions using the same syntax. The following properties are available for each dependency object:</p>

<ul>
<li><strong>src</strong>: Path to the script to load</li>
<li><strong>async</strong>: [optional] Flags if the script should load after reveal.js has started, defaults to false</li>
<li><strong>callback</strong>: [optional] Function to execute when the script has loaded</li>
